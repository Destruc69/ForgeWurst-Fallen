package net.wurstclient.forge.hacks;

import net.minecraft.client.audio.ElytraSound;
import net.minecraftforge.common.MinecraftForge;
import net.minecraftforge.fml.common.eventhandler.SubscribeEvent;
import net.wurstclient.fmlevents.WPacketInputEvent;
import net.wurstclient.fmlevents.WPacketOutputEvent;
import net.wurstclient.fmlevents.WUpdateEvent;
import net.wurstclient.forge.Category;
import net.wurstclient.forge.Hack;
import net.wurstclient.forge.settings.EnumSetting;
import net.wurstclient.forge.utils.ChatUtils;
import net.wurstclient.forge.utils.KeyBindingUtils;

public final class Debuger extends Hack {
	public Debuger() {
		super("PacketLogger", "See what packets the client is sending and recieving.");
		setCategory(Category.PLAYER);
	}

	@Override
	protected void onEnable() {
		MinecraftForge.EVENT_BUS.register(this);
	}

	@Override
	protected void onDisable() {
		MinecraftForge.EVENT_BUS.unregister(this);
	}

	@SubscribeEvent
	public void onOut(WPacketOutputEvent event) {
		ChatUtils.message(event.getPacket().toString());
	}
	@SubscribeEvent
	public void onIn(WPacketInputEvent event) {
		ChatUtils.message(event.getPacket().toString());
	}
}